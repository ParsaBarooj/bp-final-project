void DrawSimpleMap(void)
{

    int tileSize = 30;
    int startY = 200;
    int recWidth = 20;
    int recHeight = 20;

    for (int y = 0; y < recWidth; y++)
    {
        for (int x = 0; x < recHeight; x++)
        {
            int screenX =  (x * tileSize);
            int screenY =  (y * tileSize);

            if (map[y][x] == 1)
            {
                DrawRectangle(screenX, screenY, tileSize, tileSize, BLACK);
                DrawRectangleLines(screenX, screenY, tileSize, tileSize, BLACK);
            }
            else
            {
                DrawRectangle(screenX, screenY, tileSize, tileSize, DARKGRAY);
                DrawRectangleLines(screenX, screenY, tileSize, tileSize, GRAY);
            }
        }
    }
}
int IsWall(int mapX, int mapY) {
    if (mapX < 0 || mapX >= 600 || mapY < 0 || mapY >= 600) {
        return 0;
    }
    return !(map[mapY][mapX] == 0);
}